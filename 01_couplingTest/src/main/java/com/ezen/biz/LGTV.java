package com.ezen.biz;

public class LGTV {
	public void turnOn() {
		System.out.println("LGTV--전원을 켠다.");
	}
	public void turnOff() {
		System.out.println("LGTV--전원을 끈다.");
	}
	public void soundUp() {
		System.out.println("LGTV--소리를 올린다.");
	}
	public void soundDown() {
		System.out.println("LGTV--소리를 내린다.");
	}
}
